package class_one;

public class class_ifelse {
    public static void main(String[] args) {
        int time = 10;
        if (time <= 10){
            System.out.println("before 10");
        }else if (time > 10 && time < 20){
            System.out.println("between 11~19");
        }else {
            System.out.println("after 20");
        }
    }
}
